#include "libft.h"

void	ft_putchar(int c)
{
	int		cu;
	char	str[4];
	int		i;
	int		size;

	cu = c;
	i = 0;
	if (cu > 127 && (size = 2))
	{
		if (cu > 2047 && (size = 3))
		{
			if (cu > 65535 && (++size) && (str[i++] = ((cu >> 18) & 7) | 240))
				str[i++] = ((cu >> 12) & 63) | 128;
			else
				str[i++] = ((cu >> 12) & 15) | 224;
			str[i++] = ((cu >> 6) & 63) | 128;
		}
		else
			str[i++] = ((cu >> 6) & 31) | 192;
		str[i++] = (cu & 63) | 128;
		write(1, &str, size);
	}
	else
		write(1, &c, 1);
}
