#include "libft.h"

void	ft_strclr(char *s)
{
	int		size;
	int		i;

	i = 0;
	if (!(s))
		return ;
	size = ft_strlen(s);
	while (i < size)
	{
		s[i] = '\0';
		i++;
	}
}
