#include "libft.h"

char	*ft_strdup(const char *s)
{
	int		i;
	int		i2;
	char	*dest;

	i = 0;
	i2 = ft_strlen(s);
	if (!(dest = (char*)malloc(sizeof(*dest) * i2 + 1)))
		return (NULL);
	while (s[i])
	{
		dest[i] = s[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}
