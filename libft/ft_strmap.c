#include "libft.h"

char	*ft_strmap(char const *s, char (*f)(char))
{
	char	*dest;
	int		i;

	i = 0;
	if (!(s))
		return (NULL);
	if (!(dest = (char *)malloc(sizeof(*dest) * ft_strlen(s) + 1)))
		return (NULL);
	while (s[i])
	{
		dest[i] = f(s[i]);
		i++;
	}
	dest[i] = '\0';
	return (dest);
}
